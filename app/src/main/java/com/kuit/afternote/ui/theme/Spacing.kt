/**
 * 디자인 시스템 Spacing 토큰
 *
 * 매직 넘버를 피하고 일관된 간격을 유지하기 위한 spacing 값들입니다.
 * 스크롤 컨테이너 내부에서 고정값 Spacer를 사용할 때 이 토큰을 사용해야 합니다.
 */
object Spacing {
    /** Extra Small: 4dp */
    val xs = 4.dp

    /** Small: 8dp */
    val s = 8.dp

    /** Medium: 16dp */
    val m = 16.dp

    /** Large: 24dp */
    val l = 24.dp

    /** Extra Large: 32dp */
    val xl = 32.dp
}
